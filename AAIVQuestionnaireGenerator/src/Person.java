
public class Person {
	
	private int num;
	private String name;
	private String url;
	
	public Person(int num, String name, String url) {
		this.num = num;
		this.name = name;
		this.url = url;
	}

	public int getNum() {
		return num;
	}

	public String getName() {
		return name;
	}

	public String getUrl() {
		return url;
	}
	
	
}
